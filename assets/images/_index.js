export function getImage(name, nested = images) {
    for (const key in nested) {
        if (key === name && typeof nested[key] !== 'object') {
            return nested[key];
        } else if (typeof nested[key] === 'object') {
            const result = getImage(name, nested[key]);
            if (result !== undefined) {
                return result;
            }
        }
    }
}
